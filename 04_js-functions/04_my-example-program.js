import readline from "readline";
//  ฟังก์ชันสำหรับแปลงค่า text จากภาษาอังกฤษปกติเป็นให้อยู่ในรูปแบบ Kebab case หรือ Snake Case เพื่อเอาไว้ตั้งชื่อไฟล์ง่าย ๆ

function cleanFileName(str, separator = "-") {
  return (
    str
      // 1. .trim(): ตัดช่องว่างส่วนเกินที่อยู่หน้าสุดและหลังสุดของข้อความออก
      .trim()

      // 2. .toLowerCase(): แปลงตัวอักษรภาษาอังกฤษทั้งหมดให้เป็นตัวพิมพ์เล็ก
      // เพื่อป้องกันปัญหา Case-sensitive ในระบบไฟล์บางประเภท
      .toLowerCase()

      // 3. .replace(/[^a-z0-9]+/g, separator):
      // - [^a-z0-9] หมายถึง "อะไรก็ตามที่ไม่ใช่ตัวอักษร a-z หรือตัวเลข 0-9" (รวมถึงช่องว่างด้วย)
      // - เครื่องหมาย + หมายถึง "ถ้าเจออักขระพวกนี้ติดกันหลายตัว ให้มองเป็นกลุ่มเดียว"
      // - เครื่องหมาย g (Global) หมายถึง "ให้ค้นหาและแทนที่ทั้งข้อความ ไม่ใช่แค่จุดแรกที่เจอ"
      // ผลลัพธ์: อักขระพิเศษและช่องว่างทั้งหมดจะถูกเปลี่ยนเป็นตัวเชื่อม (เช่น '-' หรือ '_')
      .replace(/[^a-z0-9]+/g, separator)

      // 4. .replace(new RegExp(`\\${separator}+`, "g"), separator):
      // - ใช้สร้าง Regex แบบ Dynamic เพื่อหาตัวเชื่อมที่อาจจะซ้ำกัน เช่น "test---file"
      // - แทนที่ตัวเชื่อมที่ติดกันหลายอัน ให้เหลือเพียงอันเดียว เพื่อความสวยงาม
      // - **คำสั่ง New เอาไว้ใช้ทำอะไร ต้องศึกษาเพิ่ม**
      .replace(new RegExp(`\\${separator}+`, "g"), separator)

      // 5. .replace(new RegExp(`^\\${separator}|\\${separator}$`, "g"), ""):
      // - ^\\${separator} หมายถึง "ถ้าเจอตัวเชื่อมที่หน้าสุดของข้อความ"
      // - | หมายถึง "หรือ"
      // - \\${separator}$ หมายถึง "ถ้าเจอตัวเชื่อมที่หลังสุดของข้อความ"
      // - ให้แทนที่ด้วยค่าว่าง ("") เพื่อลบมันทิ้งไป
      // - **คำสั่ง New เอาไว้ใช้ทำอะไร ต้องศึกษาเพิ่ม**
      .replace(new RegExp(`^\\${separator}|\\${separator}$`, "g"), "")
  );
}

// สร้าง Interface สำหรับรับ-ส่งข้อมูลผ่าน Terminal
const rl = readline.createInterface({
  input: process.stdin, // รับข้อมูลจาก Keyboard
  output: process.stdout, // แสดงผลออกทางหน้าจอ
});

// ขั้นตอนที่ 1: ถามชื่อไฟล์ที่ต้องการแปลง
rl.question("ป้อนชื่อไฟล์ที่ต้องการ (Raw Name): ", function (nameInput) {
  // ขั้นตอนที่ 2: ถามประเภทตัวเชื่อมที่ต้องการ
  rl.question(
    "เลือกตัวเชื่อม (กด 1 สำหรับ '-' / กด 2 สำหรับ '_'): ",
    function (choice) {
      // ตรวจสอบเงื่อนไข: ถ้าเลือก 2 ให้ใช้ "_" นอกนั้นให้ใช้ "-" เป็นค่าเริ่มต้น
      // เครื่องหมาย `?` และ `:` คือ การใช้ if-else แบบย่อ ๆ โครงสร้างคือ `<เงื่อนไขทดสอบ> ? <ผลหากจริง> : <ผลหากเท็จ>`
      const separator = choice === "2" ? "_" : "-";

      // เรียกใช้ฟังก์ชัน cleanFileName พร้อมส่งค่าที่ได้รับจาก User เข้าไป
      const result = cleanFileName(nameInput, separator);

      // แสดงผลลัพธ์ที่ได้
      console.log("\n--- ชื่อไฟล์ที่แนะนำ ---");
      console.log(`> ${result}`);
      console.log("-----------------------\n");

      // ปิด Interface การรับข้อมูล (ถ้าไม่ปิด โปรแกรมจะไม่จบการทำงาน)
      rl.close();
    },
  );
});
